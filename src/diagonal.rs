//! Contains constants for light diagonal lines.

/// Light Diagonal Upper Right To Lower Left "╱"
/// Codepoint (Hex): U+2571
pub const LIGHT_DIAGONAL_UPPER_RIGHT_TO_LOWER_LEFT: &str = "╱";

/// Light Diagonal Upper Left To Lower Right "╲"
/// Codepoint (Hex): U+2572
pub const LIGHT_DIAGONAL_UPPER_LEFT_TO_LOWER_RIGHT: &str = "╲";

/// Light Diagonal Cross "╳"
/// Codepoint (Hex): U+2573
pub const LIGHT_DIAGONAL_CROSS: &str = "╳";
