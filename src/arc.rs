//! Contains constants for light, rounded corners.

/// Light Arc Down and Right "╭"
/// Codepoint (Hex): U+256D
pub const LIGHT_ARC_DOWN_AND_RIGHT: &str = "╭";

/// Light Arc Down and Left "╮"
/// Codepoint (Hex): U+256E
pub const LIGHT_ARC_DOWN_AND_LEFT: &str = "╮";

/// Light Arc Up and Left "╯"
/// Codepoint (Hex): U+256F
pub const LIGHT_ARC_UP_AND_LEFT: &str = "╯";

/// Light Arc Up and Right "╰"
/// Codepoint (Hex): U+2570
pub const LIGHT_ARC_UP_AND_RIGHT: &str = "╰";
