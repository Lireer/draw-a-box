//! Contains constants for dashed lines with differing numbers of dashes.

/// Light Double Dash Horizontal "╌"
/// Codepoint (Hex): U+254C
pub const LIGHT_DOUBLE_DASH_HORIZONTAL: &str = "╌";

/// Heavy Double Dash Horizontal "╍"
/// Codepoint (Hex): U+254D
pub const HEAVY_DOUBLE_DASH_HORIZONTAL: &str = "╍";

/// Light Double Dash Vertical "╎"
/// Codepoint (Hex): U+254E
pub const LIGHT_DOUBLE_DASH_VERTICAL: &str = "╎";

/// Heavy Double Dash Vertical "╏"
/// Codepoint (Hex): U+254F
pub const HEAVY_DOUBLE_DASH_VERTICAL: &str = "╏";

/// Light Triple Dash Horizontal "┄"
/// Codepoint (Hex): U+2504
pub const LIGHT_TRIPLE_DASH_HORIZONTAL: &str = "┄";

/// Heavy Triple Dash Horizontal "┅"
/// Codepoint (Hex): U+2505
pub const HEAVY_TRIPLE_DASH_HORIZONTAL: &str = "┅";

/// Light Triple Dash Vertical "┆"
/// Codepoint (Hex): U+2506
pub const LIGHT_TRIPLE_DASH_VERTICAL: &str = "┆";

/// Heavy Triple Dash Vertical "┇"
/// Codepoint (Hex): U+2507
pub const HEAVY_TRIPLE_DASH_VERTICAL: &str = "┇";

/// Light Quadruple Dash Horizontal "┈"
/// Codepoint (Hex): U+2508
pub const LIGHT_QUADRUPLE_DASH_HORIZONTAL: &str = "┈";

/// Heavy Quadruple Dash Horizontal "┉"
/// Codepoint (Hex): U+2509
pub const HEAVY_QUADRUPLE_DASH_HORIZONTAL: &str = "┉";

/// Light Quadruple Dash Vertical "┊"
/// Codepoint (Hex): U+250A
pub const LIGHT_QUADRUPLE_DASH_VERTICAL: &str = "┊";

/// Heavy Quadruple Dash Vertical "┋"
/// Codepoint (Hex): U+250B
pub const HEAVY_QUADRUPLE_DASH_VERTICAL: &str = "┋";
